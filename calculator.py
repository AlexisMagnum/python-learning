def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

result1 = add(5, 3)
result2 = subtract(10, 4)
print(f"5 + 3 = {result1}")
print(f"10 - 4 = {result2}")

def multiply(a, b):
    return a * b

result3 = multiply(3, 4)
print(f"3 * 4 = {result3}")

def delay(a, b):
    return a / b

result3 = delay(3, 4)
print(f"3 / 4 = {result3}")

